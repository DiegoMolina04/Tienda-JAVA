    public int verificarUsuario(String nombreComprobar, String contraseñaComprobar, int tipo){ //Metodo que valida el usuario ingresado.
        
        int flag = 0; //Variable que identificara si el usuario se encontro en la lista o no.
        for (int i = 0; i < usuario.size(); i++) {
            
            String nombre = usuario.get(i).getNombre(); //Se toma el nombre.
            String contraseña = usuario.get(i).getContraseña(); //Se toma la contraseña.
            int clase = usuario.get(i).getTipo(); //Se toma el tipo de usuario
            
            if (nombreComprobar.equals(nombre)&&contraseñaComprobar.equals(contraseña)&&clase==1){ /*Si la información ingresada en los campos es igual a
                                                                                                     un usuario guardado le da a la variable flag valor de 1*/
                
                flag = 1; //Si es propietario
                
            }else if (nombreComprobar.equals(nombre)&&contraseñaComprobar.equals(contraseña)&&clase==2){
                
                flag = 2; //Si es cliente
            }else{
                //Si esta errado envia 0
            }
            
        }
        
        return flag;
    }

