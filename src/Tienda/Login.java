    private void btn_aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_aceptarActionPerformed
        
        String nombreComprobar = txt_ingreseUsuario.getText();
        String contraseñaComprobar = txt_ingreseContraseña.getText();
        int clase = cmb_usuario.getSelectedIndex();

        int verificacion = objLogin.verificarUsuario(nombreComprobar, contraseñaComprobar, clase); //Llamado a la función verificadUsuario.

        if(clase==verificacion){
            switch (verificacion) {
            case 1://Si es propietario
                VistaPropietario objPropiertario = new VistaPropietario();
                objPropiertario.setVisible(true); //Se abre la vista de propietario.
                dispose(); //Se cierra la vista de Login.
            break;
            
            case 2://Si es cliente
                
                VistaCliente objCliente = new VistaCliente();                
                objCliente.usuarioProducto.setText(nombreComprobar); //
                objCliente.usuarioFiltrar.setText(nombreComprobar);  // Se le asigna al label el nombre del cliente
                objCliente.usuarioComprar.setText(nombreComprobar);  //
                objCliente.setVisible(true); //Se abre la vista cliente
                dispose();
                
            break;
            
            default://Si esta errada la información ingresada.
                JOptionPane.showMessageDialog(null, "Error ! Verifique datos ingresados","SuperTienda",  JOptionPane.ERROR_MESSAGE);
            break;
        }
        }else{
            JOptionPane.showMessageDialog(null, "Error ! Verifique datos ingresados","SuperTienda",  JOptionPane.ERROR_MESSAGE);
        }
                
    }//GEN-LAST:event_btn_aceptarActionPerformed

