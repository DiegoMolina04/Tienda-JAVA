    public int verificarProducto(String nombreComprobar, String precioComprobar, int tipo){ //Verifica si el producto se encuentra guardado.
        
        int flag = 0;
        for (int i = 0; i < producto.size(); i++) {
            
            String nombre = producto.get(i).getProducto();
            int aux = producto.get(i).getPrecio();
            String precio = String.valueOf(aux);
            int clase = producto.get(i).getCategoria();
            
            if (nombreComprobar.equals(nombre)&&precioComprobar.equals(precio)&&tipo==clase){

                posicion = i;
                flag = 1;
                
            }
            
        }
        
        return flag;
    }
    
