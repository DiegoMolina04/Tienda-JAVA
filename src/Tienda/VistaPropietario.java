    private void btn_agregarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarProductoActionPerformed
        
        String nombre = txt_ingresarProducto.getText();
        String precio = txt_ingresarPrecio.getText();
        int precioValor = Integer.parseInt(precio);
        int tipo = cmb_categoria.getSelectedIndex();       
        
        if(nombre.equals("Ingrese el nombre del producto")||precio.equals("Ingrese el valor del producto")||tipo==0){ //Verifica que no hayan campos vacios.
            
            JOptionPane.showMessageDialog(null, "Error ! Verifique datos ingresados","SuperTienda",  JOptionPane.ERROR_MESSAGE);
            
        }else{
           
            Producto nuevoProducto = new Producto(nombre, tipo, precioValor);
            producto.add(nuevoProducto); //Agrega el producto a la lista.
            pila.push(nuevoProducto); //Agrega el producto a la pila.
            Collections.sort(producto); //Se ordenan los productos ingresados por categoria.
            JOptionPane.showMessageDialog(null, "Producto agregado correctamente","SuperTienda",  JOptionPane.INFORMATION_MESSAGE);
            txt_ingresarProducto.setText("");//
            txt_ingresarPrecio.setText(""); // Se colocan los valores por defecto.
            cmb_categoria.setSelectedIndex(0);//
            holder = new PlaceHolder(txt_ingresarProducto, "Ingrese el nombre del producto");//
            holder = new PlaceHolder(txt_ingresarPrecio, "Ingrese el valor del producto");//
  
        }
        
    }//GEN-LAST:event_btn_agregarProductoActionPerformed

