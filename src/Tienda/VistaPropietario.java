    private void btn_aceptarModificacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_aceptarModificacionActionPerformed
        
        String nombre = txt_nombreModificar.getText();
        String precio = txt_precioModificacion.getText();
        int tipo = cmb_modificarEliminar.getSelectedIndex();
        
        int verificacion = verificarProducto(nombre, precio, tipo); //Se verifica si el producto existe.
        
        if(verificacion == 1){ //Si existe
            
            ModificarProducto objModificar = new ModificarProducto();
            objModificar.txt_traeNombre.setText(nombre); //
            objModificar.txt_traePrecio.setText(precio); // Toma los valores ingresados y se los pasa a la vista ModificarProducto.
            objModificar.cmb_modificarCategoria.setSelectedIndex(tipo); //
            objModificar.setVisible(true);
            dispose();
            
        }else{ //Si no existe
            
            JOptionPane.showMessageDialog(null, "Error ! Producto no encontrado","SuperTienda",  JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btn_aceptarModificacionActionPerformed

    private void btn_actualizarListaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_actualizarListaActionPerformed
        
        //Se usa para imprimir toda la lista y colocarlo en el txtArea.
        StringBuilder strBuilder = new StringBuilder();
        
        for (int i = 0; i < producto.size(); i++) {
             
            if(producto.size()==0){
                
                txtArea_mostrarProducto.setText("No hay productos ingresados");
                
            }else{
                
                String nombre = producto.get(i).getProducto();
                //String precio = producto.get(i).getPrecio();
                int precio = producto.get(i).getPrecio();
                int categoria = producto.get(i).getCategoria();

                switch(categoria){

                    case 1:
                        strBuilder.append("Nombre: "+nombre+" Precio: $"+precio+" Categoria: Camisa"+"\n");
                    break;

                    case 2:
                        strBuilder.append("Nombre: "+nombre+" Precio: $"+precio+" Categoria: Camiseta"+"\n");
                    break;

                    case 3:
                        strBuilder.append("Nombre: "+nombre+" Precio: $"+precio+" Categoria: Pantalón"+"\n");
                    break;

                    case 4:
                        strBuilder.append("Nombre: "+nombre+" Precio: $"+precio+" Categoria: Vestido"+"\n");
                    break;

                    case 5:
                        strBuilder.append("Nombre: "+nombre+" Precio: $"+precio+" Categoria: Zapatos"+"\n");
                    break;

                    case 6:
                        strBuilder.append("Nombre: "+nombre+" Precio: $"+precio+" Categoria: Chaqueta"+"\n");
                    break;

                    case 7:
                        strBuilder.append("Nombre: "+nombre+" Precio: $"+precio+" Categoria: Medias"+"\n");
                    break;
            }
            }
        }
        
        txtArea_mostrarProducto.setText(strBuilder.toString());
        
    }//GEN-LAST:event_btn_actualizarListaActionPerformed

