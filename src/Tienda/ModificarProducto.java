    private void btn_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editarActionPerformed
        
        String producto = txt_traeNombre.getText();
        String precio2 = txt_traePrecio.getText();
        int tipo2 = cmb_modificarCategoria.getSelectedIndex();
        
        if(producto.equals("Ingrese el nombre del producto")||precio2.equals("Ingrese el valor del producto")||tipo2==0){ //Verifica que no existan campos vacios.
            
            JOptionPane.showMessageDialog(null, "Error ! Verifique datos ingresados","SuperTienda",  JOptionPane.ERROR_MESSAGE);
            
        }else{
        
        String decision = JOptionPane.showInputDialog("¿Seguro desea modificar el producto? Si/No");
        
        if(decision.toLowerCase().equals("si")){
            
            //Se toman los nuevos datos ingresados
            int aux = VistaPropietario.posicion;
            String nombre = txt_traeNombre.getText(); 
            String precio= txt_traePrecio.getText();
            int precioValor = Integer.parseInt(precio);
            int tipo = cmb_modificarCategoria.getSelectedIndex();
            Producto productoModificado = new Producto(nombre, tipo, precioValor);

            VistaPropietario.producto.set(aux, productoModificado); //Se envian los nuevos datos ingresados en la misma posición.

            VistaPropietario objPropietario = new VistaPropietario();
            objPropietario.setVisible(true); //Se devuelve a la vista propietario
            dispose();// Se cierra la vista ModificarProducto.
            
        }else if(decision.toLowerCase().equals("no")){
            
        }else{
            
            JOptionPane.showMessageDialog(null, "Error ! Respuesta no valida","SuperTienda",  JOptionPane.ERROR_MESSAGE);
            
        }
        }
        
    }//GEN-LAST:event_btn_editarActionPerformed

