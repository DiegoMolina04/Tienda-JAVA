    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        
        String decision = JOptionPane.showInputDialog("¿Seguro desea eliminar el producto? Si/No");
        
        if(decision.toLowerCase().equals("si")){
            
            int aux = VistaPropietario.posicion;
            VistaPropietario.producto.remove(aux); //Se elimina la posición de la lista.
            VistaPropietario objPropietario = new VistaPropietario();
            objPropietario.setVisible(true); //Se devuleve a la vista propietario.
            dispose(); //Se cierra la vista ModificarProducto.
            
        }else if(decision.toLowerCase().equals("no")){
            
        }else{
            
            JOptionPane.showMessageDialog(null, "Error ! Respuesta no valida","SuperTienda",  JOptionPane.ERROR_MESSAGE);
            
        }
        
    }//GEN-LAST:event_btn_eliminarActionPerformed

